def rel(path: Path | str) -> str:
    p = Path(path)
    if not p.is_absolute():
        p = ROOT / p
    try:
        return str(p.relative_to(ROOT)).replace("/", "\\")
    except ValueError:
        return str(p).replace("/", "\\")


def posix_rel(path: Path | str) -> str:
    return rel(path).replace("\\", "/")


def repo_path(path: str | Path) -> Path:
    p = Path(str(path).replace("\\", os.sep).replace("/", os.sep))
    return p if p.is_absolute() else ROOT / p


def sha256(path: Path) -> str | None:
    if not path.exists() or not path.is_file():
        return None
    h = hashlib.sha256()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()


def artifact(path: Path | str) -> dict[str, Any]:
    p = repo_path(path)
    exists = p.exists() and p.is_file()
    return {
        "path": rel(p),
        "exists": exists,
        "bytes": p.stat().st_size if exists else None,
        "sha256": sha256(p) if exists else None,
    }


def strip_tex_comments(text: str) -> str:
    lines: list[str] = []
    for line in text.splitlines():
        escaped = False
        cut = len(line)
        for i, ch in enumerate(line):
            if ch == "\\":
                escaped = not escaped
                continue
            if ch == "%" and not escaped:
                cut = i
                break
            escaped = False
        lines.append(line[:cut])
    return "\n".join(lines)


def active_artifacts() -> dict[str, list[dict[str, Any]]]:
    tex = strip_tex_comments((PAPER / "main.tex").read_text(encoding="utf-8", errors="replace"))
    tables: list[dict[str, Any]] = []
    figures: list[dict[str, Any]] = []
    import re

    for i, line in enumerate(tex.splitlines(), 1):
        for m in re.finditer(r"\\input\{([^}]+)\}", line):
            target = m.group(1)
            p = PAPER / (target if target.endswith(".tex") else target + ".tex")
            if target.startswith("tables/"):
                tables.append({"line": i, "content": m.group(0), "path": rel(p)})
        for m in re.finditer(r"\\includegraphics(?:\[[^\]]*\])?\{([^}]+)\}", line):
            target = m.group(1)
            p = PAPER / target
            figures.append({"line": i, "content": m.group(0), "path": rel(p)})
    return {"tables": tables, "figures": figures}


