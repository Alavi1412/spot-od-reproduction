def main() -> int:
    args = build_parser().parse_args()
    cfg = load_yaml(Path(args.config))
    dataset_cfg = parse_dataset_config(cfg["simulation"])
    dyn = dataset_cfg.dynamics
    stations = dataset_cfg.stations
    meas_std = dataset_cfg.measurement_noise.std_vector
    baseline_cfg = parse_baseline_config(cfg["baselines"])
    pukf_rule = json.loads(Path(args.pukf_rule).read_text())
    pukf_th = pukf_rule["thresholds"]

    device_str = args.device
    if device_str == "auto":
        device_str = "cuda" if torch.cuda.is_available() else "cpu"
    device = torch.device(device_str)

    n_stations = len(stations)
    eval_start = 11

    # --- Generate train/cv/test trajectories (same seeds as faithful) -----
    train_rng = np.random.default_rng(args.train_seed)
    test_rng = np.random.default_rng(args.test_seed)
    init_pos_sigma = baseline_cfg.ukf.init_pos_std_m
    init_vel_sigma = baseline_cfg.ukf.init_vel_std_mps

    def _gen(rng, n):
        return [
            base._generate_one(
                rng,
                dataset_cfg.orbit_sampling,
                stations,
                dyn,
                meas_std,
                dataset_cfg.measurement_noise.outlier_prob,
                dataset_cfg.measurement_noise.outlier_scale,
                dataset_cfg.measurement_noise.random_dropout_prob,
                init_pos_sigma,
                init_vel_sigma,
            )
            for _ in range(n)
        ]

    print(f"[KNet-DG] device={device}, generating data", flush=True)
    t0 = time.perf_counter()
    train_bundles = _gen(train_rng, args.n_train)
    cv_bundles = _gen(train_rng, args.n_cv)
    test_bundles = _gen(test_rng, args.n_test)
    t_gen = time.perf_counter() - t0

    # Visibility-augmented inputs (n=40) for design gap 3(a).
    train_input, train_target, train_vis, train_x0 = dc._prepare_input_target_augmented(
        train_bundles, n_stations
    )
    cv_input, cv_target, cv_vis, cv_x0 = dc._prepare_input_target_augmented(cv_bundles, n_stations)
    test_input, test_target, test_vis, test_x0 = dc._prepare_input_target_augmented(
        test_bundles, n_stations
    )

    # Design gap 1: noise-equivalent observation normalization using the
    # documented measurement-noise standard deviations.
    scale_arr = _compute_noise_equivalent_scale(meas_std, n_stations)
    scale = torch.from_numpy(scale_arr.astype(np.float32))
    train_input = _apply_noise_equivalent_norm(train_input, scale, train_vis, n_stations)
    cv_input = _apply_noise_equivalent_norm(cv_input, scale, cv_vis, n_stations)
    test_input = _apply_noise_equivalent_norm(test_input, scale, test_vis, n_stations)

    # --- Build hardened KalmanNet + system model ---
    T = dyn.steps
    sys_funcs = NoiseEquivalentVisibilityAugmentedSystemFunctions(
        stations=stations,
        times_s=np.arange(T, dtype=np.float64) * dyn.dt_s,
        dt_s=dyn.dt_s,
        ballistic_coeff_m2_per_kg=dyn.ballistic_coeff_m2_per_kg,
        drag_rho_ref=dyn.drag_rho_ref,
        drag_h_ref_m=dyn.drag_h_ref_m,
        drag_scale_height_m=dyn.drag_scale_height_m,
    )
    sys_funcs.attach_noise_scale(scale)
    m1x_0 = torch.zeros(6, 1)
    m2x_0 = torch.eye(6) * (init_pos_sigma ** 2)
    Q = torch.eye(6)
    R = torch.eye(sys_funcs.obs_dim)
    sys_model = base.SPOTODSystemModel(sys_funcs, T=T, T_test=T, m1x_0=m1x_0, m2x_0=m2x_0, Q=Q, R=R)

    knet = VisibilityGatedKNetWrapper()

    class _Args:
        pass

    knet_args = _Args()
    knet_args.use_cuda = device.type == "cuda"
    knet_args.n_batch = max(args.n_batch, args.n_cv)
    knet_args.in_mult_KNet = args.in_mult
    knet_args.out_mult_KNet = args.out_mult
    knet.NNBuild(sys_model, knet_args)
    knet.attach_sys_funcs(sys_funcs)
    n_params = sum(p.numel() for p in knet.parameters() if p.requires_grad)
    print(
        f"[KNet-DG] params={n_params}, T={T}, m={sys_model.m}, n={sys_model.n}",
        flush=True,
    )

    # --- Train ---
    t0 = time.perf_counter()
    train_out = _train_hardened(
        knet=knet,
        sys_funcs=sys_funcs,
        train_input=train_input,
        train_target=train_target,
        train_vis=train_vis,
        train_x0=train_x0,
        cv_input=cv_input,
        cv_target=cv_target,
        cv_vis=cv_vis,
        cv_x0=cv_x0,
        m=sys_model.m,
        n_steps=args.n_steps,
        warmup_steps=args.warmup_steps,
        curriculum_stage_a_steps=args.curriculum_stage_a_steps,
        n_batch=args.n_batch,
        base_lr=args.base_lr,
        wd=args.wd,
        device=device,
        eval_start=eval_start,
        cv_every=args.cv_every,
    )
    t_train = time.perf_counter() - t0

    # --- Predict on test ---
    knet.batch_size = test_input.shape[0]
    knet_pred = _predict_hardened(knet, sys_funcs, test_input, test_vis, test_x0, device, T, sys_model.m)

    # --- Classical references on the same test population -----------------
    states_all = np.stack([b["states"] for b in test_bundles], axis=0)
    meas_all = np.stack([b["measurements"] for b in test_bundles], axis=0)
    vis_all = np.stack([b["visibility"] for b in test_bundles], axis=0)
    times_all = np.tile(np.arange(T, dtype=np.float64) * dyn.dt_s, (len(test_bundles), 1))
    x0_est_all = np.stack([b["x0_est"] for b in test_bundles], axis=0)

    ekf_kwargs = dict(
        ballistic_coeff_m2_per_kg=dyn.ballistic_coeff_m2_per_kg,
        drag_rho_ref=dyn.drag_rho_ref,
        drag_h_ref_m=dyn.drag_h_ref_m,
        drag_scale_height_m=dyn.drag_scale_height_m,
        enable_third_body=dyn.enable_third_body,
        enable_srp=dyn.enable_srp,
        srp_area_to_mass_m2_per_kg=dyn.srp_area_to_mass_m2_per_kg,
        srp_cr=dyn.srp_cr,
        sun_initial_phase_rad=dyn.sun_initial_phase_rad,
        moon_initial_phase_rad=dyn.moon_initial_phase_rad,
    )
    ekf_cfg = EKFConfig(
        q_pos_m=baseline_cfg.ekf.q_pos_m,
        q_vel_mps=baseline_cfg.ekf.q_vel_mps,
        init_pos_std_m=baseline_cfg.ekf.init_pos_std_m,
        init_vel_std_mps=baseline_cfg.ekf.init_vel_std_mps,
        gating_threshold=baseline_cfg.ekf.gating_threshold,
        angle_deweight_elev_cap_deg=getattr(baseline_cfg.ekf, "angle_deweight_elev_cap_deg", None),
    )
    ukf_cfg = UKFConfig(
        q_pos_m=baseline_cfg.ukf.q_pos_m,
        q_vel_mps=baseline_cfg.ukf.q_vel_mps,
        init_pos_std_m=baseline_cfg.ukf.init_pos_std_m,
        init_vel_std_mps=baseline_cfg.ukf.init_vel_std_mps,
        alpha=baseline_cfg.ukf.alpha,
        beta=baseline_cfg.ukf.beta,
        kappa=baseline_cfg.ukf.kappa,
        angle_deweight_elev_cap_deg=getattr(baseline_cfg.ukf, "angle_deweight_elev_cap_deg", None),
    )
    aukf_cfg = AdaptiveUKFConfig(
        q_pos_m=baseline_cfg.aukf.q_pos_m,
        q_vel_mps=baseline_cfg.aukf.q_vel_mps,
        init_pos_std_m=baseline_cfg.aukf.init_pos_std_m,
        init_vel_std_mps=baseline_cfg.aukf.init_vel_std_mps,
        alpha=baseline_cfg.aukf.alpha,
        beta=baseline_cfg.aukf.beta,
        kappa=baseline_cfg.aukf.kappa,
        adapt_rate=baseline_cfg.aukf.adapt_rate,
        min_r_scale=baseline_cfg.aukf.min_r_scale,
        max_r_scale=baseline_cfg.aukf.max_r_scale,
        huber_kappa=baseline_cfg.aukf.huber_kappa,
        nis_soft_gate=baseline_cfg.aukf.nis_soft_gate,
        angle_deweight_elev_cap_deg=getattr(baseline_cfg.aukf, "angle_deweight_elev_cap_deg", None),
    )
    pukf_cfg = ProcessNoiseAdaptiveUKFConfig(
        q_pos_m=baseline_cfg.ukf.q_pos_m,
        q_vel_mps=baseline_cfg.ukf.q_vel_mps,
        init_pos_std_m=baseline_cfg.ukf.init_pos_std_m,
        init_vel_std_mps=baseline_cfg.ukf.init_vel_std_mps,
        alpha=baseline_cfg.ukf.alpha,
        beta=baseline_cfg.ukf.beta,
        kappa=baseline_cfg.ukf.kappa,
        window_size=int(pukf_th["window_size"]),
        nis_per_update_expected=float(pukf_th["nis_per_update_expected"]),
        nis_warn_ratio=float(pukf_th["nis_warn_ratio"]),
        nis_alarm_ratio=float(pukf_th["nis_alarm_ratio"]),
        q_scale_warn=float(pukf_th["q_scale_warn"]),
        q_scale_alarm=float(pukf_th["q_scale_alarm"]),
        q_scale_max=float(pukf_th["q_scale_max"]),
        smoothing=float(pukf_th["smoothing"]),
        angle_deweight_elev_cap_deg=getattr(baseline_cfg.ukf, "angle_deweight_elev_cap_deg", None),
    )

    ekf_pred = np.zeros_like(states_all)
    ukf_pred = np.zeros_like(states_all)
    aukf_pred = np.zeros_like(states_all)
    pukf_pred = np.zeros_like(states_all)
    for i in range(len(test_bundles)):
        ekf_pred[i], _ = run_ekf(
            measurements=meas_all[i],
            visibility=vis_all[i],
            times_s=times_all[i],
            stations=stations,
            meas_std_vector=meas_std,
            x0_est=x0_est_all[i],
            cfg=ekf_cfg,
            **ekf_kwargs,
        )
        ukf_pred[i], _ = run_ukf(
            measurements=meas_all[i],
            visibility=vis_all[i],
            times_s=times_all[i],
            stations=stations,
            meas_std_vector=meas_std,
            x0_est=x0_est_all[i],
            cfg=ukf_cfg,
            **ekf_kwargs,
        )
        aukf_pred[i], _ = run_adaptive_ukf(
            measurements=meas_all[i],
            visibility=vis_all[i],
            times_s=times_all[i],
            stations=stations,
            meas_std_vector=meas_std,
            x0_est=x0_est_all[i],
            cfg=aukf_cfg,
            **ekf_kwargs,
        )
        pukf_pred[i], _, _ = run_process_noise_adaptive_ukf(
            measurements=meas_all[i],
            visibility=vis_all[i],
            times_s=times_all[i],
            stations=stations,
            meas_std_vector=meas_std,
            x0_est=x0_est_all[i],
            cfg=pukf_cfg,
            **ekf_kwargs,
        )

    metrics = {
        "KalmanNet-DesignGap": base._per_traj_observed_pos_rmse(
            states_all, knet_pred, vis_all, eval_start
        ),
        "EKF": base._per_traj_observed_pos_rmse(states_all, ekf_pred, vis_all, eval_start),
        "UKF": base._per_traj_observed_pos_rmse(states_all, ukf_pred, vis_all, eval_start),
        "AUKF": base._per_traj_observed_pos_rmse(states_all, aukf_pred, vis_all, eval_start),
        "PUKF": base._per_traj_observed_pos_rmse(states_all, pukf_pred, vis_all, eval_start),
    }
    means = {k: float(np.nanmean(v)) for k, v in metrics.items()}
    medians = {k: float(np.nanmedian(v)) for k, v in metrics.items()}

    classical_means = {k: v for k, v in means.items() if k != "KalmanNet-DesignGap"}
    best_classical = min(classical_means, key=lambda k: classical_means[k])
    diffs = metrics["KalmanNet-DesignGap"] - metrics[best_classical]
    mean_d, lo, hi = base._paired_bootstrap_ci(
        diffs, n_boot=int(args.bootstrap_samples), seed=args.test_seed
    )
    finite = diffs[np.isfinite(diffs)]
    paired = {
        "best_classical": best_classical,
        "best_classical_mean_m": float(classical_means[best_classical]),
        "mean_minus_best_m": float(mean_d),
        "ci_lo_m": float(lo),
        "ci_hi_m": float(hi),
        "n_paired": int(finite.size),
        "knet_better_count": int(np.sum(finite < 0.0)),
        "knet_better_rate_percent": float(100.0 * np.mean(finite < 0.0))
        if finite.size
        else float("nan"),
    }

    # Predeclared outcome classification.
    floor_pct = 3.0  # 3% practical-significance floor (matches manuscript-wide rule)
    floor_m = floor_pct / 100.0 * classical_means[best_classical]
    if not np.isfinite(lo) or not np.isfinite(hi):
        outcome = "indeterminate"
    elif lo > 0:
        outcome = "bounded_negative"
    elif hi < 0 and abs(mean_d) >= floor_m:
        outcome = "bounded_positive_above_floor"
    elif hi < 0:
        outcome = "bounded_positive_below_floor"
    else:
        outcome = "inconclusive_ci_contains_zero"

    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    payload: dict[str, Any] = {
        "schema_version": "kalmannet_design_gap_sensitivity_v1",
        "scenario": "kalmannet_design_gap_sensitivity",
        "audit_category": "post_review_pre_run_external_comparator_sensitivity",
        "vendor_commit": base.VENDOR_COMMIT,
        "vendor_path": str(base.VENDOR_ROOT.relative_to(base.REPO_ROOT)),
        "device": str(device),
        "n_params_kalmannet": int(n_params),
        "config": {
            "n_train": int(args.n_train),
            "n_cv": int(args.n_cv),
            "n_test": int(args.n_test),
            "n_steps": int(args.n_steps),
            "warmup_steps": int(args.warmup_steps),
            "curriculum_stage_a_steps": int(args.curriculum_stage_a_steps),
            "n_batch": int(args.n_batch),
            "base_lr": float(args.base_lr),
            "wd": float(args.wd),
            "in_mult": int(args.in_mult),
            "out_mult": int(args.out_mult),
            "train_seed": int(args.train_seed),
            "test_seed": int(args.test_seed),
            "T": int(T),
            "m": int(sys_model.m),
            "n": int(sys_model.n),
            "eval_start_step": int(eval_start),
            "cv_every": int(args.cv_every),
        },
        "design_gaps_addressed": {
            "orbital_scale_normalization": True,
            "sequence_length_and_curriculum_rematching": True,
            "sparse_observation_architectural_adaptation": True,
            "learning_rate_and_budget_recalibration": True,
        },
        "classical_baselines_mean_observed_step_rmse_m": classical_means,
        "best_classical_baseline": best_classical,
        "elapsed_seconds": {
            "data_generation": float(t_gen),
            "training": float(t_train),
        },
        "training_history": train_out["history"],
        "best_cv_mse": float(train_out["best_cv"]),
        "observed_step_rmse_mean_m": means,
        "observed_step_rmse_median_m": medians,
        "paired_vs_best_classical": paired,
        "practical_significance_floor_percent": float(floor_pct),
        "practical_significance_floor_m": float(floor_m),
        "predeclared_outcome_class": outcome,
        "predeclared_rule_path": args.predeclared_rule,
        "predeclared_rule_digest_sha256": hashlib.sha256(
            Path(args.predeclared_rule).read_bytes()
        ).hexdigest(),
    }

    out_json = out_dir / "design_gap_sensitivity.json"
    out_json.write_text(json.dumps(payload, indent=2, default=float))

    rows = []
    for i in range(len(test_bundles)):
        row: dict[str, Any] = {"trajectory_index": i}
        for k, arr in metrics.items():
            row[f"{k}_observed_pos_rmse_m"] = float(arr[i]) if np.isfinite(arr[i]) else None
        rows.append(row)
    pd.DataFrame(rows).to_csv(out_dir / "design_gap_sensitivity.csv", index=False)

    print(json.dumps(
        {
            "observed_step_rmse_mean_m": means,
            "paired_vs_best_classical": paired,
            "predeclared_outcome_class": outcome,
            "device": str(device),
            "vendor_commit": base.VENDOR_COMMIT,
            "elapsed_seconds": payload["elapsed_seconds"],
        },
        indent=2,
        default=float,
    ))
    return 0


