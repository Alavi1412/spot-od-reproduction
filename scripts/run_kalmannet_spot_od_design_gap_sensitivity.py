# --- Noise-equivalent observation normalization (Design gap 1) -----------


def _compute_noise_equivalent_scale(
    meas_std_vector: np.ndarray, n_stations: int
) -> np.ndarray:
    """Per-channel divisor that brings the line-of-sight measurement-noise
    standard deviation to unit scale in the SCALED observation coordinates.

    The existing MEAS_SCALE convention divides each LoS channel by a fixed
    constant (range / 1e7 m, az / pi, el / pi/2, range-rate / 1e4 m/s); in
    those scaled coordinates the per-channel measurement-noise standard
    deviations are heterogeneous (range channel ~3e-6, angle channels ~1e-4,
    range-rate ~8e-6). The upstream KalmanNet linear-canonical benchmark
    assumes unit-scale innovation noise; this divisor restores that on the
    SPOT-OD measurement model. The divisor uses the documented measurement
    noise standard deviations from the experiment configuration, NOT any
    train-set statistics, so it is deterministic given the configuration.

    Returns: shape (4*n_stations,), one positive divisor per LoS channel.
    """
    base = meas_std_vector / MEAS_SCALE  # (4,) per-station scaled noise std
    base = np.clip(base, 1e-12, None)
    return np.tile(base, n_stations)


def _apply_noise_equivalent_norm(
    obs: torch.Tensor, scale: torch.Tensor, vis: torch.Tensor, n_stations: int
) -> torch.Tensor:
    """Divide each LoS channel by its noise-equivalent scale; preserve the
    invisible-channel zero-pad and the visibility-flag block.

    obs shape: (N, 4*S+S, T); vis shape: (N, S, T).
    """
    out = obs.clone()
    obs_block = out[:, : 4 * n_stations, :]  # (N, 4*S, T)
    vis_per_channel = vis.unsqueeze(2).expand(-1, -1, 4, -1).reshape(
        obs.shape[0], 4 * n_stations, obs.shape[-1]
    )
    normed = obs_block / scale[: 4 * n_stations].view(1, -1, 1)
    # Invisible entries are zero in input; division leaves them zero; we
    # multiply by the visibility mask to be defensive against any divisor
    # interaction with stray non-zero invisible entries.
    out[:, : 4 * n_stations, :] = normed * vis_per_channel
    return out


# --- Noise-equivalent visibility-augmented system functions (Gaps 1+3) ----


class NoiseEquivalentVisibilityAugmentedSystemFunctions(dc.VisibilityAugmentedSPOTODSystemFunctions):
    """System functions that emit the noise-equivalent-normalized
    visibility-augmented observation block so h(x) sees the same channel
    scale as the input feed. The invisible-channel zero-pad convention is
    preserved: invisible blocks are zero in BOTH the observation and the
    predicted observation, so the innovation is exactly zero on invisible
    channels by construction. The visibility-flag block is unchanged.
    """

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._scale: np.ndarray | None = None

    def attach_noise_scale(self, scale: torch.Tensor) -> None:
        self._scale = scale.detach().cpu().numpy().astype(np.float64)

    def f_torch(self, x: torch.Tensor) -> torch.Tensor:
        # Clamp the scaled state into a physically plausible band before the
        # numerical propagator so a transient bad gain cannot drive the
        # propagator through overflow. Positions are divided by 1e7 m and
        # velocities by 1e4 m/s, so |x| at most ~1 for LEO; band of 2.0
        # admits any plausible LEO state but excludes pathological divergence.
        x_clamped = torch.clamp(x, min=-2.0, max=2.0)
        return super().f_torch(x_clamped)

    def h_torch(self, x: torch.Tensor) -> torch.Tensor:
        out = super().h_torch(x)  # zero-padded visibility-augmented
        if self._scale is None:
            return out
        device = out.device
        dtype = out.dtype
        was_2d = out.dim() == 2
        if was_2d:
            out = out.unsqueeze(0)
        out_np = out.detach().cpu().numpy()
        n_st = self.n_stations
        out_np[:, : 4 * n_st, 0] = out_np[:, : 4 * n_st, 0] / self._scale[: 4 * n_st]
        t_out = torch.from_numpy(out_np).to(device=device, dtype=dtype)
        if was_2d:
            t_out = t_out.squeeze(0)
        return t_out


# --- Visibility-gated KalmanNet wrapper (Design gap 3, part b) ------------


class VisibilityGatedKNetWrapper(base.KNetSPOTODWrapper):
    """At zero-visibility steps the learned correction is force-gated to the
    prior prediction. The underlying KalmanNet gain still runs (its hidden
    state continues to evolve) but its output state estimate is replaced by
    the prior so the learned correction cannot act on open-loop propagation
    steps. Implements design gap 3, part (b)."""

    def attach_step_visibility(self, step_visibility: np.ndarray) -> None:
        # Boolean visibility per (batch, T) — 1 means at least one station
        # visible at that step. Used inside the training/predict loops.
        self._step_visibility = np.asarray(step_visibility, dtype=np.float64)

    def gate_with_prior(self, x_post: torch.Tensor, x_prior: torch.Tensor, batch_visible: torch.Tensor) -> torch.Tensor:
        """Return x_post on visible steps and x_prior on invisible steps.

        batch_visible shape: (B,) 0/1 visibility flag for the current step.
        """
        b = x_post.shape[0]
        mask = batch_visible.view(b, 1, 1).to(x_post.device).to(x_post.dtype)
        return mask * x_post + (1.0 - mask) * x_prior


# --- Two-stage visibility-stratified curriculum (Design gap 2) -----------


def _select_high_visibility_indices(
    train_vis: torch.Tensor, eval_start: int, tercile: float = 1.0 / 3.0
) -> np.ndarray:
    """Return indices of the top-tercile training trajectories by per-trajectory
    visibility fraction over the evaluation window.

    train_vis shape: (N, S, T). Visibility fraction is the fraction of
    eval-window steps with at least one visible station.
    """
    vis_per_step = (train_vis.sum(dim=1) > 0.5).float()  # (N, T)
    frac = vis_per_step[:, eval_start:].mean(dim=1).cpu().numpy()
    n = frac.shape[0]
    k = max(1, int(math.ceil(tercile * n)))
    return np.argsort(-frac)[:k]


# --- Cosine schedule with warm-up (Design gap 4) -------------------------


def _cosine_lr(step: int, total: int, base_lr: float, warmup_frac: float) -> float:
    warm = max(1, int(round(warmup_frac * total)))
    if step <= warm:
        return base_lr * step / warm
    progress = (step - warm) / max(1, total - warm)
    return 0.5 * base_lr * (1.0 + math.cos(math.pi * progress))


# --- Hardened training loop ----------------------------------------------


def _train_hardened(
    knet: VisibilityGatedKNetWrapper,
    sys_funcs: StandardizedVisibilityAugmentedSystemFunctions,
    train_input: torch.Tensor,
    train_target: torch.Tensor,
    train_vis: torch.Tensor,
    train_x0: torch.Tensor,
    cv_input: torch.Tensor,
    cv_target: torch.Tensor,
    cv_vis: torch.Tensor,
    cv_x0: torch.Tensor,
    *,
    m: int,
    n_steps: int,
    warmup_steps: int,
    curriculum_stage_a_steps: int,
    n_batch: int,
    base_lr: float,
    wd: float,
    device: torch.device,
    eval_start: int,
    cv_every: int,
) -> dict[str, Any]:
    """Hardened training loop that addresses all four design gaps."""
    knet.to(device)
    optimizer = torch.optim.Adam(knet.parameters(), lr=base_lr, weight_decay=wd)
    loss_fn = nn.MSELoss(reduction="mean")
    best_cv = float("inf")
    best_state = None
    history: list[dict[str, Any]] = []
    n_train = train_input.shape[0]
    n_cv = cv_input.shape[0]
    T = train_input.shape[-1]

    # Design gap 2: stage A trains only on the top-tercile-visibility subset.
    stage_a_indices = _select_high_visibility_indices(train_vis, eval_start)
    print(f"[KNet-DG] curriculum stage A indices: {len(stage_a_indices)}/{n_train}", flush=True)

    # Precompute per-trajectory per-step visibility (>=1 station visible).
    train_step_vis = (train_vis.sum(dim=1) > 0.5).float().cpu().numpy()  # (N, T)
    cv_step_vis = (cv_vis.sum(dim=1) > 0.5).float().cpu().numpy()  # (Ncv, T)

    rng_py = np.random.default_rng(0)
    last_cv = float("inf")
    for step_i in range(1, n_steps + 1):
        if step_i <= curriculum_stage_a_steps:
            pool = stage_a_indices
            stage_label = "A"
        else:
            pool = np.arange(n_train)
            stage_label = "B"
        bs = min(n_batch, pool.size)
        idx = rng_py.choice(pool, size=bs, replace=False)
        y_batch = train_input[idx].to(device)
        x_batch = train_target[idx].to(device)
        v_batch = train_vis[idx].to(device)
        x0_batch = train_x0[idx].to(device)
        sv_batch = train_step_vis[idx]  # (bs, T)
        b = y_batch.shape[0]

        # Cosine LR schedule (design gap 4).
        lr_t = _cosine_lr(step_i, n_steps, base_lr, warmup_frac=max(1e-6, warmup_steps / n_steps))
        for g in optimizer.param_groups:
            g["lr"] = lr_t

        knet.train()
        knet.batch_size = b
        knet.init_hidden_KNet()
        sys_funcs.visibility_mask = None
        sys_funcs.step_idx = 0
        knet.InitSequence(x0_batch, T)
        sys_funcs.reset()

        x_out = torch.zeros((b, m, T), device=device, dtype=y_batch.dtype)
        x_prev = x0_batch.squeeze(2)
        for t in range(T):
            sys_funcs.step_idx = t
            sys_funcs.set_visibility(v_batch[:, :, t].detach().cpu().numpy())
            y_t = y_batch[:, :, t].unsqueeze(2)
            # Compute prior prediction for gating: propagate x_prev one step
            # with the wrapper f (no learning).
            with torch.no_grad():
                x_prior_pred = sys_funcs.f_torch(x_prev.unsqueeze(2)).squeeze(2)
            x_t = knet(y_t).squeeze(2)
            batch_vis_t = torch.from_numpy(sv_batch[:, t]).to(device).to(x_t.dtype)
            # Design gap 3(b): force-gate learned correction at zero visibility.
            x_t = knet.gate_with_prior(x_t.unsqueeze(2), x_prior_pred.unsqueeze(2), batch_vis_t).squeeze(2)
            x_out[:, :, t] = x_t
            x_prev = x_t

        # Position+velocity loss (state loss, like upstream KalmanNet does
        # on its native benchmark) over the eval window.
        loss_pos = loss_fn(x_out[:, :3, eval_start:], x_batch[:, :3, eval_start:])
        loss_vel = loss_fn(x_out[:, 3:, eval_start:], x_batch[:, 3:, eval_start:])
        loss = loss_pos + loss_vel
        optimizer.zero_grad()
        if not torch.isfinite(loss):
            train_loss = float("inf")
            grad_norm = float("nan")
        else:
            loss.backward(retain_graph=False)
            grad_norm = float(
                torch.nn.utils.clip_grad_norm_(knet.parameters(), max_norm=1.0).item()
            )
            optimizer.step()
            train_loss = float(loss.item())

        # CV every cv_every steps (or last step). Saves cost vs every step.
        run_cv = (step_i % cv_every == 0) or (step_i == n_steps) or (step_i == 1)
        if run_cv:
            knet.eval()
            knet.batch_size = n_cv
            knet.init_hidden_KNet()
            sys_funcs.visibility_mask = None
            sys_funcs.step_idx = 0
            knet.InitSequence(cv_x0.to(device), T)
            sys_funcs.reset()
            with torch.no_grad():
                x_cv = torch.zeros((n_cv, m, T), device=device, dtype=cv_input.dtype)
                x_prev = cv_x0.to(device).squeeze(2)
                for t in range(T):
                    sys_funcs.step_idx = t
                    sys_funcs.set_visibility(cv_vis[:, :, t].detach().cpu().numpy())
                    y_t = cv_input[:, :, t].to(device).unsqueeze(2)
                    x_prior_pred = sys_funcs.f_torch(x_prev.unsqueeze(2)).squeeze(2)
                    x_t = knet(y_t).squeeze(2)
                    batch_vis_t = torch.from_numpy(cv_step_vis[:, t]).to(device).to(x_t.dtype)
                    x_t = knet.gate_with_prior(x_t.unsqueeze(2), x_prior_pred.unsqueeze(2), batch_vis_t).squeeze(2)
                    x_cv[:, :, t] = x_t
                    x_prev = x_t
                cv_pos = float(
                    loss_fn(x_cv[:, :3, eval_start:], cv_target[:, :3, eval_start:].to(device)).item()
                )
                cv_vel = float(
                    loss_fn(x_cv[:, 3:, eval_start:], cv_target[:, 3:, eval_start:].to(device)).item()
                )
                cv_loss = cv_pos + cv_vel
            last_cv = cv_loss
            if cv_loss < best_cv:
                best_cv = cv_loss
                best_state = {k: v.clone().detach().cpu() for k, v in knet.state_dict().items()}
        else:
            cv_loss = last_cv

        history.append(
            {
                "step": step_i,
                "stage": stage_label,
                "lr": lr_t,
                "train_mse": train_loss,
                "cv_mse": cv_loss,
                "grad_norm": grad_norm,
            }
        )

        if step_i % 25 == 0 or step_i == 1 or step_i == n_steps:
            print(
                f"[KNet-DG] step {step_i} stage={stage_label} lr={lr_t:.3e} "
                f"train_mse={train_loss:.6f} cv_mse={cv_loss:.6f} best={best_cv:.6f}",
                flush=True,
            )

    if best_state is not None:
        knet.load_state_dict(best_state)
    return {"history": history, "best_cv": best_cv}


# --- Prediction with visibility-gating ------------------------------------


def _predict_hardened(
    knet: VisibilityGatedKNetWrapper,
    sys_funcs: StandardizedVisibilityAugmentedSystemFunctions,
    test_input: torch.Tensor,
    test_vis: torch.Tensor,
    test_x0: torch.Tensor,
    device: torch.device,
    T: int,
    m: int,
) -> np.ndarray:
    knet.eval()
    n = test_input.shape[0]
    knet.batch_size = n
    knet.init_hidden_KNet()
    sys_funcs.visibility_mask = None
    sys_funcs.step_idx = 0
    knet.InitSequence(test_x0.to(device), T)
    sys_funcs.reset()
    test_step_vis = (test_vis.sum(dim=1) > 0.5).float().cpu().numpy()  # (n, T)
    with torch.no_grad():
        out = torch.zeros((n, m, T), device=device, dtype=test_input.dtype)
        x_prev = test_x0.to(device).squeeze(2)
        for t in range(T):
            sys_funcs.step_idx = t
            sys_funcs.set_visibility(test_vis[:, :, t].detach().cpu().numpy())
            y_t = test_input[:, :, t].to(device).unsqueeze(2)
            x_prior_pred = sys_funcs.f_torch(x_prev.unsqueeze(2)).squeeze(2)
            x_t = knet(y_t).squeeze(2)
            batch_vis_t = torch.from_numpy(test_step_vis[:, t]).to(device).to(x_t.dtype)
            x_t = knet.gate_with_prior(x_t.unsqueeze(2), x_prior_pred.unsqueeze(2), batch_vis_t).squeeze(2)
            out[:, :, t] = x_t
            x_prev = x_t
    arr = out.detach().cpu().numpy().transpose(0, 2, 1)  # (n, T, m) scaled
    return arr * STATE_SCALE.astype(arr.dtype)


