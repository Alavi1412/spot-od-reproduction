def _custom_train_eval(
    knet: KNetSPOTODWrapper,
    sys_model: SPOTODSystemModel,
    sys_funcs: SPOTODSystemFunctions,
    train_input: torch.Tensor,
    train_target: torch.Tensor,
    train_vis: torch.Tensor,
    train_x0: torch.Tensor,
    cv_input: torch.Tensor,
    cv_target: torch.Tensor,
    cv_vis: torch.Tensor,
    cv_x0: torch.Tensor,
    *,
    n_steps: int,
    n_batch: int,
    lr: float,
    wd: float,
    device: torch.device,
    eval_start: int,
) -> dict[str, Any]:
    """Custom training loop faithful to Pipeline_EKF.NNTrain semantics.

    This calls the upstream KNet_step forward and computes MSE on state,
    with held-out CV-best checkpointing (identical to upstream). The only
    change is that for each example in the batch we set the visibility mask
    BEFORE every step's forward pass so ``h(x)`` masks invisible stations
    consistently with the input vector zeros.
    """
    knet.to(device)
    optimizer = torch.optim.Adam(knet.parameters(), lr=lr, weight_decay=wd)
    loss_fn = nn.MSELoss(reduction="mean")
    best_cv = float("inf")
    best_state = None
    history = []
    n_train = train_input.shape[0]
    n_cv = cv_input.shape[0]
    T = train_input.shape[-1]
    m = sys_model.m

    rng_py = np.random.default_rng(0)
    for step_i in range(n_steps):
        # --- Training batch ---
        idx = rng_py.choice(n_train, size=min(n_batch, n_train), replace=False)
        y_batch = train_input[idx].to(device)
        x_batch = train_target[idx].to(device)
        v_batch = train_vis[idx].to(device)
        x0_batch = train_x0[idx].to(device)
        b = y_batch.shape[0]

        knet.train()
        knet.batch_size = b
        knet.init_hidden_KNet()
        # Clear visibility mask before InitSequence so h(x0) (which is used to
        # populate y_previous inside KalmanNet) does not index a stale mask
        # from a previous batch of different size.
        sys_funcs.visibility_mask = None
        sys_funcs.step_idx = 0
        # Initialize posterior with the noisy initial state estimate
        knet.InitSequence(x0_batch, T)
        sys_funcs.reset()
        x_out = torch.zeros((b, m, T), device=device, dtype=y_batch.dtype)
        for t in range(T):
            sys_funcs.step_idx = t
            sys_funcs.set_visibility(v_batch[:, :, t].detach().cpu().numpy())
            y_t = y_batch[:, :, t].unsqueeze(2)
            x_t = knet(y_t)  # advances sys_funcs.step_idx by 1
            x_out[:, :, t] = torch.squeeze(x_t, 2)
        # MSE over the eval window only (observed-step convention applied at
        # evaluation; training loss uses the same eval window so the network
        # is not steered by the propagator-only steps).
        loss = loss_fn(x_out[:, :3, eval_start:], x_batch[:, :3, eval_start:])
        optimizer.zero_grad()
        # Clamp NaN/Inf-poisoned loss to a finite training signal -- the
        # gain RNN can produce a transient bad gain that drives the state
        # through propagation overflow, but we keep optimisation stable.
        if not torch.isfinite(loss):
            train_loss = float("inf")
        else:
            loss.backward(retain_graph=False)
            torch.nn.utils.clip_grad_norm_(knet.parameters(), max_norm=1.0)
            optimizer.step()
            train_loss = float(loss.item())

        # --- CV ---
        knet.eval()
        knet.batch_size = n_cv
        knet.init_hidden_KNet()
        sys_funcs.visibility_mask = None
        sys_funcs.step_idx = 0
        knet.InitSequence(cv_x0.to(device), T)
        sys_funcs.reset()
        with torch.no_grad():
            x_cv = torch.zeros((n_cv, m, T), device=device, dtype=cv_input.dtype)
            for t in range(T):
                sys_funcs.step_idx = t
                sys_funcs.set_visibility(cv_vis[:, :, t].detach().cpu().numpy())
                y_t = cv_input[:, :, t].to(device).unsqueeze(2)
                x_t = knet(y_t)
                x_cv[:, :, t] = torch.squeeze(x_t, 2)
            cv_loss = float(
                loss_fn(x_cv[:, :3, eval_start:], cv_target[:, :3, eval_start:].to(device)).item()
            )
        history.append({"step": step_i, "train_mse": train_loss, "cv_mse": cv_loss})
        if cv_loss < best_cv:
            best_cv = cv_loss
            best_state = {k: v.clone().detach().cpu() for k, v in knet.state_dict().items()}
        print(
            f"[KNet-SPOT-OD] step {step_i}: train_mse={train_loss:.4f}, cv_mse={cv_loss:.4f}, best={best_cv:.4f}",
            flush=True,
        )

    if best_state is not None:
        knet.load_state_dict(best_state)
    return {"history": history, "best_cv": best_cv}


def _predict(
    knet: KNetSPOTODWrapper,
    sys_funcs: SPOTODSystemFunctions,
    test_input: torch.Tensor,
    test_vis: torch.Tensor,
    test_x0: torch.Tensor,
    device: torch.device,
    T: int,
    m: int,
) -> np.ndarray:
    knet.eval()
    n = test_input.shape[0]
    knet.batch_size = n
    knet.init_hidden_KNet()
    sys_funcs.visibility_mask = None
    sys_funcs.step_idx = 0
    knet.InitSequence(test_x0.to(device), T)
    sys_funcs.reset()
    with torch.no_grad():
        out = torch.zeros((n, m, T), device=device, dtype=test_input.dtype)
        for t in range(T):
            sys_funcs.step_idx = t
            sys_funcs.set_visibility(test_vis[:, :, t].detach().cpu().numpy())
            y_t = test_input[:, :, t].to(device).unsqueeze(2)
            x_t = knet(y_t)
            out[:, :, t] = torch.squeeze(x_t, 2)
    # Return shape (n, T, m) in PHYSICAL units (unscale).
    arr = out.detach().cpu().numpy().transpose(0, 2, 1)  # (n, T, m) scaled
    return arr * STATE_SCALE.astype(arr.dtype)


