"""Faithful KalmanNet transposition to the SPOT-OD measurement setting (Loop 42).

The Loop 41 reviewer correctly noted that the official KalmanNet reproduction
runs only on the linear-canonical benchmark at reduced scale; the central
learned-vs-classical negative on SPOT-OD therefore remained bounded to the
authors' own constructions plus one in-house learned-gain comparator. This
script transposes the *official* KalmanNet architecture from the vendored
upstream (``external/third_party/KalmanNet_TSP``; pinned commit recorded in
``external/third_party/KalmanNet_TSP_COMMIT``) to the SPOT-OD measurement
setting.

Faithfulness is structural: the trained KalmanGain-RNN network is used
unmodified (``KalmanNet_nn.KalmanNetNN``); the trainer is the upstream
``Pipeline_EKF.Pipeline_EKF`` with its (Q,Sigma,S)-GRU stack, MSE state loss,
held-out validation-best checkpointing, and the architecture
hyperparameters (``in_mult_KNet``, ``out_mult_KNet``) untouched.

What necessarily adapts (documented honestly in the paper and result.md):

* **State dimension** ``m=6`` (ECI position+velocity) instead of the
  benchmark ``m=2``.
* **Observation dimension** ``n=8*4=32`` (8 SPOT-OD ground stations, each
  with range/azimuth/elevation/range-rate). For invisible stations at a
  given step both the measurement vector entry and the model's predicted
  measurement entry are zeroed identically, so the innovation is exactly
  zero on invisible channels and the gain RNN sees no information to
  exploit. This is the same masking convention used by the manuscript's
  classical filters and prevents the learned gain from leaking information
  about visibility through unobserved channels.
* **State transition ``f(x)``** is the SPOT-OD nonlinear orbital dynamics
  (compact two-body + J2 + exponential-atmosphere drag, RK4 at 20 s,
  identical to every classical baseline in the manuscript). Because the
  per-step transition depends on absolute time only through the rotating
  atmosphere and the rotating measurement frame, ``f`` and ``h`` are
  parameterised by a step-counter held on the wrapper. The KalmanNet
  forward pass is unchanged.

Scope of the comparison:

* The transposition is evaluated under the **same observed-step protocol
  and trajectory population** as the manuscript's classical references
  (per-trajectory observed-step position RMSE on a disjoint-seed test
  draw). The classical EKF/UKF/AUKF/PUKF references are scored on the
  exact same population. The result is a *direct* learned-vs-classical
  comparison on SPOT-OD, removing the "no transposition" Loop 41 gap.

* If the transposition underperforms the strongest classical reference,
  this is reported honestly as a bounded external learned-OD negative and
  is the manuscript's external audit case for the protocol. It is not a
  refutation of KalmanNet in general or of any other learned-OD method.

Outputs are non-paper-facing JSON/CSV under ``results/kalmannet_spot_od/``;
the paper-facing LaTeX table is generated by ``build_paper_assets.py``.
"""
