# --- Time-varying f/h wrappers --------------------------------------------


def _wrap_az(az: float) -> float:
    if az < 0.0:
        az += 2.0 * np.pi
    if az >= 2.0 * np.pi:
        az -= 2.0 * np.pi
    return az


class SPOTODSystemFunctions:
    """Stateful f, h, and visibility mask for the SPOT-OD transposition.

    The KalmanNet forward pass calls ``f(x)`` and ``h(x)`` once per step; the
    wrapper maintains a step counter so the (time-dependent) rotation and the
    visibility mask are consistent with the underlying SPOT-OD scenario.

    For an invisible station at the current step, the predicted measurement
    block in ``h(x)`` is zeroed, and the corresponding measurement entry is
    likewise zeroed in the data feed. The innovation is then identically zero
    on invisible channels and the learned gain has no information to exploit
    on those channels.
    """

    def __init__(
        self,
        stations: tuple[StationGeometry, ...],
        times_s: np.ndarray,
        dt_s: float,
        ballistic_coeff_m2_per_kg: float,
        drag_rho_ref: float,
        drag_h_ref_m: float,
        drag_scale_height_m: float,
    ) -> None:
        self.stations = stations
        self.times_s = np.asarray(times_s, dtype=np.float64)
        self.dt = float(dt_s)
        self.ballistic = float(ballistic_coeff_m2_per_kg)
        self.rho_ref = float(drag_rho_ref)
        self.h_ref = float(drag_h_ref_m)
        self.scale_h = float(drag_scale_height_m)
        self.n_stations = len(stations)
        self.obs_dim = 4 * self.n_stations
        self.step_idx: int = 0
        # Per-step visibility mask for the *current* example/batch. Set by
        # the caller before invoking ``forward`` (KalmanNet processes one
        # example at a time inside a batch, so we use a (batch, n_stations)
        # mask per step).
        self.visibility_mask: np.ndarray | None = None  # (batch, n_stations)

    def reset(self) -> None:
        self.step_idx = 0

    def advance(self) -> None:
        self.step_idx += 1

    def set_visibility(self, mask: np.ndarray) -> None:
        # mask shape: (batch, n_stations); rows are 0/1 visibility flags.
        self.visibility_mask = np.asarray(mask, dtype=np.float64)

    # f and h must accept torch tensors as KalmanNet does. We bridge to numpy
    # for the orbital dynamics since the gain network is the only trainable
    # piece and f/h are non-differentiable bridges (true to the upstream
    # design for non-linear cases: Extended_sysmdl.py also uses numpy/torch
    # bridges with torch.no_grad).

    def f_torch(self, x: torch.Tensor) -> torch.Tensor:
        # x: [batch, m, 1] in SCALED coordinates -> [batch, m, 1] in SCALED.
        device = x.device
        was_2d = x.dim() == 2
        if was_2d:
            x = x.unsqueeze(0)
        x_np = x.detach().cpu().numpy()  # (B, m, 1)
        b = x_np.shape[0]
        out = np.zeros_like(x_np)
        # Step index is the *destination* step; propagate from t_{idx-1} to t_idx.
        idx = max(1, min(self.step_idx, len(self.times_s) - 1))
        t_prev = float(self.times_s[idx - 1])
        dt = float(self.times_s[idx] - self.times_s[idx - 1])
        for i in range(b):
            state_scaled = x_np[i, :, 0]
            state = state_scaled * STATE_SCALE  # unscale to physical units
            new = rk4_step(
                state,
                dt=dt,
                ballistic_coeff_m2_per_kg=self.ballistic,
                t_s=t_prev,
                drag_rho_ref=self.rho_ref,
                drag_h_ref_m=self.h_ref,
                drag_scale_height_m=self.scale_h,
            )
            # Guard against propagation overflow producing NaNs on huge
            # transient state perturbations from an untrained gain net.
            if not np.all(np.isfinite(new)):
                new = state
            out[i, :, 0] = new / STATE_SCALE
        t_out = torch.from_numpy(out).to(device=device, dtype=x.dtype)
        if was_2d:
            t_out = t_out.squeeze(0)
        return t_out

    def h_torch(self, x: torch.Tensor) -> torch.Tensor:
        device = x.device
        was_2d = x.dim() == 2
        if was_2d:
            x = x.unsqueeze(0)
        x_np = x.detach().cpu().numpy()  # (B, m, 1) in SCALED coordinates
        b = x_np.shape[0]
        out = np.zeros((b, self.obs_dim, 1), dtype=np.float64)
        # Clamp to valid index; InitSequence may call h(x0) outside the main loop.
        step_clamped = min(max(self.step_idx, 0), len(self.times_s) - 1)
        t_s = float(self.times_s[step_clamped])
        mask = self.visibility_mask
        for i in range(b):
            state = x_np[i, :, 0] * STATE_SCALE
            if not np.all(np.isfinite(state)):
                # Posterior diverged this step; mask everything to zero.
                continue
            for s_idx, station in enumerate(self.stations):
                if mask is not None and mask[i, s_idx] < 0.5:
                    continue  # masked invisible -> contribute zeros
                z, _ = line_of_sight_measurement(state, station, t_s)
                z = z.copy()
                z[1] = _wrap_az(float(z[1]))
                out[i, s_idx * 4 : s_idx * 4 + 4, 0] = z / MEAS_SCALE
        t_out = torch.from_numpy(out).to(device=device, dtype=x.dtype)
        if was_2d:
            t_out = t_out.squeeze(0)
        return t_out


# --- KalmanNet wrapper that advances the step counter on each forward -----


class KNetSPOTODWrapper(KalmanNetNN):
    """Thin subclass that advances the wrapper's step counter after each call.

    The upstream forward pass is unchanged; we only intercept ``forward`` to
    bump ``sys_funcs.step_idx`` so subsequent calls to ``f`` and ``h`` see
    the next step's time/visibility.
    """

    def attach_sys_funcs(self, sys_funcs: SPOTODSystemFunctions) -> None:
        self.sys_funcs = sys_funcs

    def reset_steps(self) -> None:
        self.sys_funcs.reset()

    def forward(self, y: torch.Tensor) -> torch.Tensor:
        # The caller (training/predict loop) is responsible for setting
        # ``sys_funcs.step_idx`` and ``sys_funcs.visibility_mask`` before each
        # call; we no longer auto-advance here to avoid running past T-1
        # during InitSequence and similar boundary calls.
        return super().forward(y)


# --- SystemModel facade compatible with Pipeline_EKF / KalmanNet_nn -------


class SPOTODSystemModel:
    """Minimal SystemModel shape used by KalmanNet/Pipeline_EKF.

    Provides the attributes Pipeline_EKF reads: m, n, f, h, T, T_test,
    m1x_0, plus prior_Q/prior_Sigma/prior_S used by KalmanNetNN.
    """

    def __init__(
        self,
        sys_funcs: SPOTODSystemFunctions,
        T: int,
        T_test: int,
        m1x_0: torch.Tensor,
        m2x_0: torch.Tensor,
        Q: torch.Tensor,
        R: torch.Tensor,
    ) -> None:
        self.f = sys_funcs.f_torch
        self.h = sys_funcs.h_torch
        self.m = 6
        self.n = sys_funcs.obs_dim
        self.T = T
        self.T_test = T_test
        self.m1x_0 = m1x_0
        self.m2x_0 = m2x_0
        self.Q = Q
        self.R = R
        # KalmanNetNN reads these directly when InitKGainNet is called.
        self.prior_Q = torch.eye(self.m)
        self.prior_Sigma = torch.zeros(self.m, self.m)
        self.prior_S = torch.eye(self.n)


# --- Trajectory and measurement generation (compact dynamics) -------------


def _sample_orbit(orbit_cfg: OrbitSamplingConfig, rng: np.random.Generator) -> np.ndarray:
    earth_radius_m = 6378.1363e3
    altitude_m = rng.uniform(orbit_cfg.altitude_min_km, orbit_cfg.altitude_max_km) * 1e3
    semi_major_axis_m = earth_radius_m + altitude_m
    eccentricity = rng.uniform(orbit_cfg.eccentricity_min, orbit_cfg.eccentricity_max)
    inclination_rad = np.deg2rad(
        rng.uniform(orbit_cfg.inclination_min_deg, orbit_cfg.inclination_max_deg)
    )
    raan_rad = rng.uniform(0.0, 2.0 * np.pi)
    arg_perigee_rad = rng.uniform(0.0, 2.0 * np.pi)
    true_anomaly_rad = rng.uniform(0.0, 2.0 * np.pi)
    return kepler_to_cartesian(
        semi_major_axis_m=semi_major_axis_m,
        eccentricity=eccentricity,
        inclination_rad=inclination_rad,
        raan_rad=raan_rad,
        arg_perigee_rad=arg_perigee_rad,
        true_anomaly_rad=true_anomaly_rad,
    )


def _propagate(
    x0: np.ndarray,
    dt: float,
    steps: int,
    ballistic: float,
    rho_ref: float,
    h_ref: float,
    scale_h: float,
) -> np.ndarray:
    states = np.zeros((steps, 6), dtype=np.float64)
    states[0] = x0
    for k in range(1, steps):
        states[k] = rk4_step(
            states[k - 1],
            dt=dt,
            ballistic_coeff_m2_per_kg=ballistic,
            t_s=(k - 1) * dt,
            drag_rho_ref=rho_ref,
            drag_h_ref_m=h_ref,
            drag_scale_height_m=scale_h,
        )
    return states


def _generate_one(
    rng: np.random.Generator,
    orbit_cfg: OrbitSamplingConfig,
    stations: tuple[StationGeometry, ...],
    dyn,
    meas_std: np.ndarray,
    outlier_prob: float,
    outlier_scale: float,
    dropout_prob: float,
    init_pos_sigma: float,
    init_vel_sigma: float,
) -> dict[str, np.ndarray]:
    x0 = _sample_orbit(orbit_cfg, rng)
    states = _propagate(
        x0,
        dt=dyn.dt_s,
        steps=dyn.steps,
        ballistic=dyn.ballistic_coeff_m2_per_kg,
        rho_ref=dyn.drag_rho_ref,
        h_ref=dyn.drag_h_ref_m,
        scale_h=dyn.drag_scale_height_m,
    )
    times = np.arange(dyn.steps, dtype=np.float64) * dyn.dt_s
    n_stations = len(stations)
    meas = np.zeros((dyn.steps, n_stations, 4), dtype=np.float64)
    vis = np.zeros((dyn.steps, n_stations), dtype=np.float64)
    for t_idx, t_s in enumerate(times):
        for s_idx, station in enumerate(stations):
            z_true, visible = line_of_sight_measurement(states[t_idx], station, float(t_s))
            if not visible:
                continue
            if rng.uniform() < dropout_prob:
                continue
            eps = rng.normal(0.0, meas_std)
            if rng.uniform() < outlier_prob:
                eps *= outlier_scale
            z_noisy = z_true + eps
            z_noisy[1] = _wrap_az(float(z_noisy[1]))
            z_noisy[2] = float(np.clip(z_noisy[2], -0.5 * np.pi, 0.5 * np.pi))
            meas[t_idx, s_idx] = z_noisy
            vis[t_idx, s_idx] = 1.0
    x0_perturb = np.array(
        [
            rng.normal(0.0, init_pos_sigma),
            rng.normal(0.0, init_pos_sigma),
            rng.normal(0.0, init_pos_sigma),
            rng.normal(0.0, init_vel_sigma),
            rng.normal(0.0, init_vel_sigma),
            rng.normal(0.0, init_vel_sigma),
        ]
    )
    return {
        "states": states,
        "measurements": meas,
        "visibility": vis,
        "times": times,
        "x0_est": states[0] + x0_perturb,
    }


# --- Training-data preparation: flatten 8 stations × 4 channels = 32 obs --


def _prepare_input_target(
    bundles: list[dict[str, np.ndarray]], n_stations: int
) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    """Return (y_input, x_target, visibility, x0_est) as torch tensors.

    Observation vectors and states are returned in SCALED coordinates
    (positions / 1e7, velocities / 1e4, range / 1e7, az/(pi), el/(pi/2),
    range-rate / 1e4) so the gain RNN sees unit-scale signals as the
    upstream KalmanNet linear-canonical benchmark assumes.
    """
    n = len(bundles)
    steps = bundles[0]["states"].shape[0]
    obs_dim = 4 * n_stations
    y = np.zeros((n, obs_dim, steps), dtype=np.float32)
    x = np.zeros((n, 6, steps), dtype=np.float32)
    v = np.zeros((n, n_stations, steps), dtype=np.float32)
    x0 = np.zeros((n, 6, 1), dtype=np.float32)
    meas_scale_block = np.tile(MEAS_SCALE.astype(np.float64), n_stations)  # (4*S,)
    for i, b in enumerate(bundles):
        meas = b["measurements"]  # (T, S, 4)
        vis = b["visibility"]  # (T, S)
        masked = meas * vis[..., None]
        flat = masked.reshape(steps, obs_dim)
        scaled_flat = flat / meas_scale_block  # broadcast over time
        y[i] = np.transpose(scaled_flat, (1, 0)).astype(np.float32)
        x[i] = np.transpose(b["states"] / STATE_SCALE, (1, 0)).astype(np.float32)
        v[i] = np.transpose(vis, (1, 0)).astype(np.float32)
        x0[i, :, 0] = (b["x0_est"] / STATE_SCALE).astype(np.float32)
    return (
        torch.from_numpy(y),
        torch.from_numpy(x),
        torch.from_numpy(v),
        torch.from_numpy(x0),
    )


# --- Observed-step RMSE / paired bootstrap --------------------------------


def _per_traj_observed_pos_rmse(
    states: np.ndarray, preds: np.ndarray, visibility: np.ndarray, eval_start: int
) -> np.ndarray:
    eval_vis = visibility[:, eval_start:]
    observed = np.sum(eval_vis, axis=-1) >= 0.5
    y_true = states[:, eval_start:, :3]
    y_pred = preds[:, eval_start:, :3]
    out = np.full(states.shape[0], np.nan, dtype=np.float64)
    for i in range(states.shape[0]):
        mask = observed[i]
        if not np.any(mask):
            continue
        err = y_true[i, mask] - y_pred[i, mask]
        out[i] = float(np.sqrt(np.mean(np.sum(err * err, axis=-1))))
    return out


def _paired_bootstrap_ci(
    diffs: np.ndarray, n_boot: int, seed: int, alpha: float = 0.05
) -> tuple[float, float, float]:
    finite = diffs[np.isfinite(diffs)]
    if finite.size == 0:
        return float("nan"), float("nan"), float("nan")
    rng = np.random.default_rng(seed)
    means = np.empty(n_boot, dtype=np.float64)
    for b in range(n_boot):
        idx = rng.integers(0, finite.size, finite.size)
        means[b] = float(np.mean(finite[idx]))
    return (
        float(np.mean(finite)),
        float(np.quantile(means, alpha / 2)),
        float(np.quantile(means, 1.0 - alpha / 2)),
    )


